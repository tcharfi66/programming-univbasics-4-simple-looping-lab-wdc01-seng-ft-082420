def loop_message_five_times(message)
  ctr = 0 
  while ctr < 5 do
    puts message
    ctr+= 1
  end
end


def loop_message_n_times(message,n)
  ctr = 0
  while ctr < n do 
    puts message
    ctr+= 1
  end
end

